#include "libft.h"

int	ft_isalnum(int c)
{
	int	test1;
	int	test2;

	test1 = (c < '0' || c > '9');
	test2 = ((c < 'A' || c > 'Z') && (c < 'a' || c > 'z'));
	if (test1 && test2)
		return (0);
	return (1);
}
